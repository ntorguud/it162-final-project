            <h2 id="xray">Dental X-rays</h2>
            <p>Dental radiographs (x-rays) are essential, preventative, diagnostic tools that provide valuable information not visible during a regular dental exam. Dentists and dental hygienists use this information to safely and accurately detect hidden dental abnormalities and complete an accurate treatment plan. Without x-rays, problem areas may go undetected.</p>
            <ul>
                <li>~ Abscesses or cysts.</li>
                <br>
                <li>~ Bone loss.</li>
                <br>
                <li>~ Cancerous and non-cancerous tumors.</li>
                <br>
                <li>~ Decay between the teeth.</li>
                <br>
                <li>~ Developmental abnormalities.</li>
                <br>
                <li>~ Poor tooth and root positions.</li>
                <br>
                <li>~ Problems inside a tooth or below the gum line.</li>
            </ul>

            <br>
            <h2 id="sealant">Sealants</h2>
            <p>A sealant is a thin, plastic coating applied to the chewing surface of molars, premolars and any deep grooves (called pits and fissures) of teeth. More than 75% of dental decay begins in these deep grooves. Teeth with these conditions are hard to clean and are very susceptible to decay. A sealant protects the tooth by sealing deep grooves, creating a smooth, easy to clean surface.

                Sealants can protect teeth from decay for many years, but need to be checked for wear and chipping at regular dental visits.</p>

            <h2 id="fluoride">Fluoride Treatment</h2>
            <p>Fluoride is the most effective agent available to help prevent tooth decay. It is a mineral that is naturally present in varying amounts in almost all foods and water supplies. The benefits of fluoride have been well known for over 50 years and are supported by many health and professional organizations.</p>


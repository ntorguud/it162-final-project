            <h2 id="exam">Dental Exams</h2>
            <p>A comprehensive dental exam will be performed by your dentist at your initial dental visit. At regular check-up exams, your dentist and hygienist will include the following:</p>
            <ul>
                <li>~ Examination of diagnostic x-rays (radiographs): Essential for detection of decay, tumors, cysts, and bone loss. X-rays also help determine tooth and root positions.</li>
